    private void CalculosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculosActionPerformed
        // TODO add your handling code here:
        double Ven_Cod = Double.parseDouble(Cod_Vendidos.getText());
        double Ven_Min = Double.parseDouble(Min_Vendidos.getText());
        double Ven_Fort= Double.parseDouble(Fort_Vendidos.getText());
        double precio_Cod = 34500;
        double precio_Min = 8800;
        double precio_Fort = 58200;
        DiObCod.setText(String.valueOf(Ven_Cod*precio_Cod));
        DiObMin.setText(String.valueOf(Ven_Min*precio_Min));
        DiObFort.setText(String.valueOf(Ven_Fort*precio_Fort));
        CoGaCod.setText(String.valueOf((Ven_Cod*precio_Cod)*0.06));
        CoGaMin.setText(String.valueOf((Ven_Min*precio_Min)*0.04));
        CoGaFort.setText(String.valueOf((Ven_Fort*precio_Fort)*0.09));
        TotalDineroGanado.setText(String.valueOf((Ven_Cod*precio_Cod)+(Ven_Min*precio_Min)+(Ven_Fort*precio_Fort)));
        double Com_Cod = Double.parseDouble(CoGaCod.getText());
        double Com_Min = Double.parseDouble(CoGaMin.getText());
        double Com_Fort = Double.parseDouble(CoGaFort.getText());
        ComTotal.setText(String.valueOf(Com_Cod+Com_Min+Com_Fort));
        CantJuegos.setText(String.valueOf(Ven_Cod+Ven_Min+Ven_Fort));
    }//GEN-LAST:event_CalculosActionPerformed

