    private void GuardarLosDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarLosDatosActionPerformed
        String nom;
        String DinMin;
        String ComMin;
        String DinFort;
        String ComFort;
        String DinCod;
        String ComCod;
        String TGanancias;
        String CObtenida;
        String JVendidos;
        
        nom = NombreVendedor.getText();
        DinMin = DiObMin.getText();        
        ComMin = CoGaMin.getText();        
        DinFort = DiObFort.getText();        
        ComFort = CoGaFort.getText();        
        DinCod = DiObCod.getText();        
        ComCod = CoGaCod.getText();        
        TGanancias = TotalDineroGanado.getText();
        CObtenida = ComTotal.getText();
        JVendidos = CantJuegos.getText();
        
        String bd = "reg_vendedores";
        String url ="jdbc:mysql://localhost:3306/"+bd+"?useSSL=false&serverTimezone=UTC";
        String user = "root";
        String password = "";
        String driver = "com.mysql.cj.jdbc.driver";
        String st = "INSERT INTO vendedores VALUES('"+nom+"','"+DinMin+"','"+ComMin+"','"+DinFort+"','"+ComFort+"','"+DinCod+"','"+ComCod+"','"+TGanancias+"','"+CObtenida+"','"+JVendidos+"')";
        try{
            //System.out.println("Hola");
            cx = DriverManager.getConnection(url,user,password);
            System.out.println("Se conecto a la base de datos: "+bd);
            stmt = cx.createStatement();
            stmt.executeUpdate(st);
            javax.swing.JOptionPane.showMessageDialog(this, "Se registró a la BD");
        } catch (SQLException ex){
            System.out.println("No se conecto a la Base de datos "+bd);
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE,null,ex);
        }
        try{
            cx.close();
        } catch (SQLException ex){
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE,null,ex);
        }
        
        
    }//GEN-LAST:event_GuardarLosDatosActionPerformed

